package com.setare.setare_96.store.LISTs;

public class LIST_Product {

    private String image;
    private String name;

    public LIST_Product(String image, String name) {
        this.image = image;
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
